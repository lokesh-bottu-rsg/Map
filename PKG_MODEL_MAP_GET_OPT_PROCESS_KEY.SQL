CREATE OR REPLACE PROCEDURE PKG_MODEL_MAP_GET_OPT_PROCESS_KEY(IN_MODEL_EXEC_ID INT)
RETURNS TABLE (OPT_PROCESS_KEY VARCHAR(100),EXEC_STATUS_CD VARCHAR(40))
LANGUAGE SQL AS 
DECLARE RES RESULTSET;
BEGIN
    RES := (SELECT OPT_PROCESS_KEY,EXEC_STATUS_CD FROM MODEL_EXEC WHERE MODEL_EXEC_ID = :IN_MODEL_EXEC_ID);
RETURN TABLE(RES);
END;

GRANT USAGE ON PROCEDURE PKG_MODEL_MAP_GET_OPT_PROCESS_KEY(INT) TO ROLE RA_PROMO_DEV_QA_UAT_ACCESS;
GRANT USAGE ON PROCEDURE PKG_MODEL_MAP_GET_OPT_PROCESS_KEY(INT) TO ROLE RA_PROMO_PROD_ACCESS;